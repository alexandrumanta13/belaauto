            <p>6. CE ESTE UN “COOKIE”?</p>
            <p>Un “Internet Cookie” (termen cunoscut și ca “browser cookie” sau “HTTP cookie” sau pur și simplu
                “cookie”) este un fișier de mici dimensiuni, format din litere și numere, care va fi stocat pe
                computerul, terminalul mobil sau alte echipamente ale utilizatorului de pe care se acceseaza acest site
                (mai multe detalii pe https://ro.wikipedia.org/wiki/Cookie).</p>
            <p>Cookie-ul este instalat prin solicitarea emisa de catre un web-server unui browser (ex: Internet
                Explorer, Chrome, Firefox) și este complet “pasiv” adică nu conține programe software, viruși sau
                spyware și nu poate accesa informațiile de pe hard drive-ul utilizatorului.</p>
            <p>Un cookie este format din 2 părți: numele și conținutul sau valoarea cookie-ului. Mai mult, durata de
                existență a unui cookie este determinată, iar tehnic doar web-serverul care a trimis cookie-ul îl poate
                accesa din nou în momentul în care un utilizator se întoarce pe website-ul asociat web-serverului
                respectiv.</p>
            <p>Cookie-urile în sine nu solicită informații cu caracter personal pentru a putea fi utilizate și, în cele
                mai multe cazuri, nu identifică personal utilizatorii de internet.</p>
            <p>Există 2 categorii mari de cookie-uri:</p>
            <p>Cookie-uri de sesiune – acestea sunt stocate temporar în dosarul de cookie-uri al browserului web pentru
                ca acesta să le memoreze până când utilizatorul iese de pe site-ul respectiv sau închide fereastra
                browserului.</p>
            <p>Cookie-uri persistente – acestea sunt stocate pe hard drive-ul unui computer sau echipament pentru o
                durata configurata de website-ul care l-a plasat. Acest tip de cookie-uri pot fi șterse de utilizator în
                orice moment prin intermediul setărilor browserului.</p>
